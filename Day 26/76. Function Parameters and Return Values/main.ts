/* Function Parameters and Return Values:
Create a function that takes two numbers as parameters, adds them together , and returns the result. Show how can you 
call this function and log the result.
*/

function functionWithTwoParameters(num1: number, num2: number) {
  let result = num1 + num2;
  return result; // return result
}

console.log(functionWithTwoParameters(2, 6)); // function call && log result