const BIRTHDAY = "01.08.2003"; 
const age = BIRTHDAY;
/*
Typically, we use upper case for constants that are "hard-coded", meaning the value is predetermined 
and directly embedded in the code. An example of this is a birthday, which could be represented in 
upper case. 
On the other hand, age is calculated at run-time. Although it's a constant in the sense that it doesn't 
change during the execution of the code, it's not as much of a "constant" as a birthday because it's 
computed. Therefore, we should use lower case for it.
*/